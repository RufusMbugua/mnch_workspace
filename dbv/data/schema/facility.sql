CREATE TABLE `facility` (
  `facilityID` int(11) NOT NULL AUTO_INCREMENT,
  `facilityMFC` varchar(45) NOT NULL,
  `facilityName` varchar(150) NOT NULL,
  `facilityType` varchar(55) DEFAULT NULL,
  `facilityLevel` varchar(45) DEFAULT NULL,
  `facilityProvince` varchar(45) DEFAULT NULL,
  `facilityDistrict` varchar(45) DEFAULT NULL,
  `facilityCounty` varchar(45) DEFAULT NULL,
  `facilityOwnedBy` varchar(255) DEFAULT NULL,
  `facilityInchargeContactPerson` varchar(45) DEFAULT NULL,
  `facilityInchargeEmail` varchar(100) DEFAULT NULL,
  `facilityInchargeTelephone` varchar(55) DEFAULT NULL,
  `facilityMCHContactPerson` varchar(45) DEFAULT NULL,
  `facilityMCHEmail` varchar(100) DEFAULT NULL,
  `facilityMCHTelephone` varchar(55) DEFAULT NULL,
  `facilityMaternityContactPerson` varchar(45) DEFAULT NULL,
  `facilityMaternityEmail` varchar(100) DEFAULT NULL,
  `facilityMaternityTelephone` varchar(55) DEFAULT NULL,
  `facilitySurveyStatus` varchar(15) NOT NULL DEFAULT 'pending' COMMENT 'complete,pending',
  `facilityCHSurveyStatus` varchar(15) NOT NULL DEFAULT 'pending',
  `deliveriesDone` varchar(1) NOT NULL DEFAULT 'U' COMMENT 'U-unknown (default), N-NO, Y-Yes',
  `reasonDeliveryNotDone` varchar(255) NOT NULL DEFAULT 'n/a',
  `createdAt` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updatedAt` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`facilityID`),
  UNIQUE KEY `facility_mfc_UNIQUE` (`facilityMFC`),
  KEY `fk_facility_county1` (`facilityCounty`),
  KEY `facilityDistrict` (`facilityDistrict`),
  KEY `facilityType` (`facilityType`),
  KEY `fk_facility_province1` (`facilityProvince`),
  KEY `fk_facility_facility_owner1` (`facilityOwnedBy`),
  KEY `fk_facility_facility_level1` (`facilityLevel`),
  CONSTRAINT `facility_ibfk_2` FOREIGN KEY (`facilityType`) REFERENCES `facility_type` (`facilityType`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `facility_ibfk_4` FOREIGN KEY (`facilityCounty`) REFERENCES `county` (`countyName`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `facility_ibfk_5` FOREIGN KEY (`facilityDistrict`) REFERENCES `district` (`districtName`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=latin1