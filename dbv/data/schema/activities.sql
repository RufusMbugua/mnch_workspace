CREATE TABLE `activities` (
  `activity_id` int(11) NOT NULL AUTO_INCREMENT,
  `activity_name` text,
  `activity_start` int(11) DEFAULT NULL,
  `activity_end` int(11) DEFAULT NULL,
  `activity_progress` varchar(45) DEFAULT NULL,
  `activity_created` timestamp NULL DEFAULT NULL,
  `activity_partners` varchar(45) DEFAULT NULL,
  `activity_creator` int(11) DEFAULT NULL,
  `activity_classification` varchar(45) DEFAULT NULL,
  `activity_assigned_type` int(11) DEFAULT NULL,
  `activity_responsible` int(11) DEFAULT NULL,
  `activity_indicator` int(11) DEFAULT NULL,
  `activity_objective` int(11) DEFAULT NULL,
  PRIMARY KEY (`activity_id`),
  KEY `objective_idx` (`activity_objective`),
  KEY `indicator_idx` (`activity_indicator`),
  KEY `assined_type_idx` (`activity_assigned_type`),
  KEY `users_idx` (`activity_responsible`),
  CONSTRAINT `assigned_type` FOREIGN KEY (`activity_assigned_type`) REFERENCES `assigned_type` (`assigned_id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `indicator` FOREIGN KEY (`activity_indicator`) REFERENCES `indicators` (`indicator_id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `objective` FOREIGN KEY (`activity_objective`) REFERENCES `objectives` (`objective_id`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `users` FOREIGN KEY (`activity_responsible`) REFERENCES `users` (`user_id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=latin1