CREATE TABLE `facilities` (
  `fac_id` int(11) NOT NULL AUTO_INCREMENT,
  `fac_mfl` varchar(45) NOT NULL,
  `fac_name` varchar(150) NOT NULL,
  `fac_type` varchar(55) DEFAULT NULL,
  `fac_level` varchar(45) DEFAULT NULL,
  `fac_province` varchar(45) DEFAULT NULL,
  `fac_district` varchar(45) DEFAULT NULL,
  `fac_county` varchar(45) DEFAULT NULL,
  `fac_ownership` varchar(255) DEFAULT NULL,
  `fac_incharge_contact_person` varchar(45) DEFAULT NULL,
  `fac_incharge_email` varchar(100) DEFAULT NULL,
  `fac_incharge_telephone` varchar(55) DEFAULT NULL,
  `fac_mch_contact_person` varchar(45) DEFAULT NULL,
  `fac_mch_email` varchar(100) DEFAULT NULL,
  `fac_mch_telephone` varchar(55) DEFAULT NULL,
  `fac_maternity_contact_person` varchar(45) DEFAULT NULL,
  `fac_maternity_email` varchar(100) DEFAULT NULL,
  `fac_maternity_telephone` varchar(55) DEFAULT NULL,
  `fac_created` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `fac_updated` timestamp NULL DEFAULT NULL,
  PRIMARY KEY (`fac_id`),
  UNIQUE KEY `facility_mfc_UNIQUE` (`fac_mfl`),
  KEY `fk_facility_county1` (`fac_county`),
  KEY `facilityDistrict` (`fac_district`),
  KEY `facilityType` (`fac_type`),
  KEY `fk_facility_province1` (`fac_province`),
  KEY `fk_facility_facility_owner1` (`fac_ownership`),
  KEY `fk_facility_facility_level1` (`fac_level`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1